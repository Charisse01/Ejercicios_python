# Hacer un programa que calcule y muestre en pantalla el promedio de cuatro notas de un alumno.

nota1 = int(input("Ingrese primera nota: "))
nota2 = int(input("Ingrese segunda nota: "))
nota3 = int(input("Ingrese tercera nota: "))
nota4 = int(input("Ingrese cuarta nota: "))

print(nota1 + nota2 + nota3 + nota4 / 4) 
