# Mostrar el nuevo salario de un empleado si obtuvo un aumento del 5% sobre el salario. (Nota: El salario anterior debe de ser digitado por teclado.)

salario = float(input("Ingrese su salario actual: "))

incremento = salario * .5

nuevoSalario = salario + incremento

print("Su nuevo salario con incremento es de:  $", nuevoSalario)
print("Su salario incremento un: ", incremento)

